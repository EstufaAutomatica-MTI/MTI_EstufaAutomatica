void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc)
{
	average[0] = 0; average[1] = 0;
	for(int i=0;i<SAMPLE;i++)
		{
		if((i%2)==0)average[0] += measure[i];
		else average[1] += measure[i];
		}
	Light = average[0]*PERCENT;
	Moisture = average[1]*PERCENT;
}
